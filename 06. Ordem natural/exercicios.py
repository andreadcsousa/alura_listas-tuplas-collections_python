# Organizando a lista
idades = [12, 90, 1, 8, 10, 25, 32, 31, 40, 50]

# Para ordenar sem perder a lista original utiliza-se a função "sorted()", ascendente e descendente.
sorted(idades)
sorted(idades, reverse=True)

# Para ordenar e substituir a lista original utiliza-se o método "list.sort()", ascendente e descendente.
idades.sort()
idades

idades.sort(reverse=True)
idades
